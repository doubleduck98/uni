        public override string[] GetRolesForUser(string username)
        {
            var cs = ConfigurationManager.ConnectionStrings["aspdotnetConnectionString"].ConnectionString;
            using (var ctx = new UsersDataContext(cs))
            {
                var userRoles = from user in ctx.USER where user.nazwa == username
                                join userroles in ctx.USERSROLES on user.id equals userroles.userID
                                join roles in ctx.ROLES on userroles.roleID equals roles.id
                                select roles.roleName;
                return userRoles.ToArray();
            }
        }

